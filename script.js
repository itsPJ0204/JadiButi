function filterByDisease(disease) {
    const plantList = document.getElementById("plant-list");
    const plantsToShow = explants[disease] || [];
    
    // Clear the current plant list
    plantList.innerHTML = '';
    
    // Populate the plant list with filtered plants
    plantsToShow.forEach(plant => {
        const li = document.createElement("li")
        const listItem = document.createElement("a");
        listItem.textContent = plant;
        listItem.setAttribute("href",`/plants.html?plant=${plant}`)
        li.appendChild(listItem)
        plantList.appendChild(li);
    });
}
