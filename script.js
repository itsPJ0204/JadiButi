// Function to get URL parameters
function getURLParameter(name) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(name);
}

// Function to show the plant information based on the URL parameter
function showPlantInfo() {
    const plantParam = getURLParameter('plant');
    const plantList = document.getElementById('plant-list');

    if (plantParam) {
        // Find the plant that matches the URL parameter
        const selectedPlant = plants.find(plant => plant.plant_name.toLowerCase().includes(plantParam.toLowerCase()));

        if (selectedPlant) {
            // Update page content with selected plant info
            document.getElementsByClassName('title')[0].innerHTML = selectedPlant.plant_name;
            document.getElementsByClassName('heading')[0].innerHTML = selectedPlant.plant_name;
            document.getElementsByClassName('uses')[0].innerHTML = selectedPlant.uses;
            document.getElementsByClassName('Cultivation')[0].innerHTML = selectedPlant.cultivation;
            document.getElementsByClassName('diseases_cured')[0].innerHTML = selectedPlant.diseases_cured;
            document.getElementsByClassName('model-container')[0].innerHTML = selectedPlant.model;
            if(selectedPlant.AR){
                const AR = document.getElementsByClassName('AR')[0] ;
                const a = document.createElement("a");
                a.setAttribute("href",`${selectedPlant.AR}`);
                a.textContent = "View in AR"
                AR.appendChild(a);
            } else{
                const AR = document.getElementsByClassName('AR')[0] ;
                AR.textContent = "AR coming soon..."
            }
        } else {
            // Handle the case where no matching plant is found
            plantList.innerHTML = '<p>Plant not found.</p>';
        }
    } else {
        // Handle the case where no 'plant' parameter is provided
        plantList.innerHTML = '<p>No plant specified.</p>';
    }
}

// Initialize function on DOMContentLoaded
document.addEventListener('DOMContentLoaded', showPlantInfo);

// Existing functions
function toggleDropdown() {
    const dropdown = document.getElementById("dropdown-content");
    dropdown.style.display = dropdown.style.display === "block" ? "none" : "block";
}

function filterPlants() {
    const query = document.querySelector('.search-bar input[type="text"]').value.toLowerCase();
    const plantListItems = document.querySelectorAll('.plant-list li');

    plantListItems.forEach((item) => {
        const plantName = item.textContent.toLowerCase();
        if (plantName.includes(query)) {
            item.style.display = 'block';
        } else {
            item.style.display = 'none';
        }
    });
}

document.querySelector('.search-bar input[type="text"]').addEventListener('input', filterPlants);
